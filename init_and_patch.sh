#!/bin/bash

#############################################################################

TAG_UHD=v3.15.0.0
TAG_FPGA=v3.15.0.0

#############################################################################

git submodule init
git submodule update

(
    cd uhd-src

    git checkout $TAG_UHD

    patch -p0 < ../host.patch

    #########################################################################

    git submodule init
    git submodule update

    (
        cd fpga-src

        git checkout $TAG_FPGA

        patch -p0 < ../../fpga.patch
    )

    #########################################################################
)

